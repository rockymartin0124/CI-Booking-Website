$(document).ready(function() {
  // onclick behavior
  $('.change_img img').click( function() {
    //var lang = $(this).attr('id');
    var lang = $(this).attr('alt'); // obtain language id
    // translate all translatable elements
    $('.tr').each(function(i){
      if($(this).is( "input" ) || $(this).is( "textarea" )){
        $(this).attr('placeholder',aLangKeys[lang][ $(this).attr('key') ]);
      } else{
        $(this).text(aLangKeys[lang][ $(this).attr('key') ]);
      }
    });
    trans_data(lang);
  } );

  $('.change_dropdown').click( function() {
    //var lang = $(this).attr('id');
    var lang = $(this).attr('alt'); // obtain language id
    // translate all translatable elements
    $('.tr').each(function(i){
      if($(this).is( "input" ) || $(this).is( "textarea" )){
        $(this).attr('placeholder',aLangKeys[lang][ $(this).attr('key') ]);
      } else{
        $(this).text(aLangKeys[lang][ $(this).attr('key') ]);
      }
    });
    trans_data(lang);
  } );
  function trans_data(lang_str)
  {
    $.get('welcome/trans_data',
      {
        org_str: $('.content_data').eq(0).text(),
        lang: lang_str
      },
      function (res){
        $('.content_data').eq(0).text(res);
      }
    );
    $.get('welcome/trans_data',
      {
        org_str: $('.content_data').eq(1).text(),
        lang: lang_str
      },
      function (res){
        $('.content_data').eq(1).text(res);
      }
    );
    $.get('welcome/trans_data',
      {
        org_str: $('.content_data').eq(2).text(),
        lang: lang_str
      },
      function (res){
        $('.content_data').eq(2).text(res);
      }
    );
  }
});  

